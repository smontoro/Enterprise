      <h3>
         <strong>Our Impact</strong>
      </h3>

      <div style="padding: 10px;">
         <p>A job during high school can put a young person on the path to a fulfilling future. Multiple research studies show that:</p>

         <ul>
            <li>Young people who work during high school are more responsible and have better time and financial management skills.</li>
            <br>
            <li>A job in high school has a positive impact on future earnings and occupational status.</li>
            <br>
            <li>There is a strong link between working during high school and attaining a job right after graduation.</li>
            </br>
            <li>Low income youth who work a moderate amount while in high school are 50% more likely to graduate high school.</li>
         </ul>

         <div style="text-align: center;">
